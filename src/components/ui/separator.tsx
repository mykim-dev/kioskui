"use client"

import * as React from "react"
import { Separator as SeparatorPrimitive } from "@base-ui/react"

import { cn } from "@/lib/utils"

function Separator({
  className,
  orientation = "horizontal",
  // NOTE: Base UI Separator doesn't support `decorative` (Radix prop). We keep it here for API compatibility.
  decorative: _decorative = true,
  ...props
}: React.ComponentProps<typeof SeparatorPrimitive> & { decorative?: boolean }) {
  void _decorative

  return (
    <SeparatorPrimitive
      data-slot="separator"
      orientation={orientation}
      className={cn(
        "bg-border shrink-0 data-[orientation=horizontal]:h-px data-[orientation=horizontal]:w-full data-[orientation=vertical]:h-full data-[orientation=vertical]:w-px",
        className
      )}
      {...props}
    />
  )
}

export { Separator }
